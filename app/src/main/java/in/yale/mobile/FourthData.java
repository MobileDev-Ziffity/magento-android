package in.yale.mobile;


public class FourthData {

    private String fourthValue, fourthLabel;

    FourthData(String fourthValue, String fourthLabel) {
        this.fourthValue = fourthValue;
        this.fourthLabel = fourthLabel;
    }

    String getFourthValue() {
        return fourthValue;
    }

    public void setFourthValue(String fourthValue) {
        this.fourthValue = fourthValue;
    }

    String getFourthLabel() {
        return fourthLabel;
    }

    public void setFourthLabel(String fourthLabel) {
        this.fourthLabel = fourthLabel;
    }
}


